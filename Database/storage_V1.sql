CREATE PROC GET_CURRENT_CODE_SUPPLIER @KEY_CODE CHAR(3)
AS
BEGIN
	SELECT MAX(RIGHT(CODE, 4)) FROM SUPPLIER 
	WHERE CODE LIKE '%' + @KEY_CODE
END
