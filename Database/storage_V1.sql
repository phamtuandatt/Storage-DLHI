CREATE PROC GET_ITEMS
AS
BEGIN
	SELECT ITEM.ID, CODE, ITEM.NAME, UNIT.NAME, PICTURE, GROUPS.NAME, SUPPLIER.NAME_COMPANY_SUPPLIER
	FROM ITEM, UNIT, GROUPS, SUPPLIER
	WHERE ITEM.UNIT_ID = UNIT.ID AND ITEM.GROUP_ID = GROUPS.ID AND ITEM.SUPPLIER_ID = SUPPLIER.ID
END
GO
EXEC GET_ITEMS

